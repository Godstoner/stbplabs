# Алгоритми гешування SHA.

import hashlib
# дані, які гешуємо
print ("Введіть дані(слово або цифри), які бажаєте гешувати: ")
sha=input()


# закодувати введене слово за допомогою encode()
# потім надсилаємо SHA256()

result = hashlib.sha256 ( sha .encode ())


# Виводить эквівалетне шістнадцяткове значення.
print ( "Слово, яке гешуємо:" )

print ( sha )

print ( "Шістнадцятковий еквівалент SHA256:" )

print ( result.hexdigest())

print ( "" )

